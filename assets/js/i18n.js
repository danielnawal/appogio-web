/* ---- Engine ---- */
function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t[el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = t[el.dataset.i18nHtml];
    if (v !== undefined) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const v = t[el.dataset.i18nPlaceholder];
    if (v !== undefined) el.placeholder = v;
  });
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
  localStorage.setItem('appogio_lang', lang);
}

/* ---- Helper: set flag icon class ---- */
function setFlagClass(el, flagClass) {
  el.className = 'fi fis ' + flagClass;
}

/* ---- Switcher UI ---- */
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('appogio_lang') || 'es';

  /* --- Navbar switcher --- */
  const trigger = document.getElementById('lang-trigger');
  const menu    = document.getElementById('lang-menu');
  const flagEl  = document.getElementById('lang-flag');
  const codeEl  = document.getElementById('lang-code');

  if (trigger && menu) {
    trigger.addEventListener('click', e => {
      e.stopPropagation();
      menu.classList.toggle('open');
    });

    menu.querySelectorAll('.lang-option').forEach(btn => {
      btn.addEventListener('click', e => {
        e.stopPropagation();
        const lang = btn.dataset.lang;
        setFlagClass(flagEl, btn.dataset.flagClass);
        codeEl.textContent = btn.dataset.code;
        menu.querySelectorAll('.lang-option').forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        menu.classList.remove('open');
        syncFloat(lang);
        applyLang(lang);
      });
    });

    const savedNavBtn = menu.querySelector(`[data-lang="${saved}"]`);
    if (savedNavBtn) {
      setFlagClass(flagEl, savedNavBtn.dataset.flagClass);
      codeEl.textContent = savedNavBtn.dataset.code;
      savedNavBtn.classList.add('active');
    }
  }

  /* --- Floating corner switcher --- */
  const floatTrigger = document.getElementById('lang-float-trigger');
  const floatMenu    = document.getElementById('lang-float-menu');
  const floatFlag    = document.getElementById('float-flag');

  function syncFloat(lang) {
    if (!floatMenu) return;
    const btn = floatMenu.querySelector(`[data-lang="${lang}"]`);
    if (btn) {
      floatFlag.className = 'fi ' + btn.dataset.flagClass;
      floatMenu.querySelectorAll('.lang-float-option').forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
    }
  }

  if (floatTrigger && floatMenu) {
    floatTrigger.addEventListener('click', e => {
      e.stopPropagation();
      floatMenu.classList.toggle('open');
    });

    floatMenu.querySelectorAll('.lang-float-option').forEach(btn => {
      btn.addEventListener('click', e => {
        e.stopPropagation();
        const lang = btn.dataset.lang;
        floatFlag.className = 'fi ' + btn.dataset.flagClass;
        floatMenu.querySelectorAll('.lang-float-option').forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        floatMenu.classList.remove('open');
        /* sync navbar */
        if (flagEl && codeEl && menu) {
          const navBtn = menu.querySelector(`[data-lang="${lang}"]`);
          if (navBtn) {
            setFlagClass(flagEl, navBtn.dataset.flagClass);
            codeEl.textContent = navBtn.dataset.code;
            menu.querySelectorAll('.lang-option').forEach(b => b.classList.remove('active'));
            navBtn.classList.add('active');
          }
        }
        applyLang(lang);
      });
    });

    syncFloat(saved);
  }

  document.addEventListener('click', () => {
    if (menu) menu.classList.remove('open');
    if (floatMenu) floatMenu.classList.remove('open');
  });

  if (saved !== 'es') applyLang(saved);
});
